echo -e "$GREEN this is the GREEN color $RESET"
echo -e "$YELLOW this is the YELLOW color $RESET"
echo -e "$BLUE this is the BLUE color $RESET"
echo -e "$MAGENTA this is the MAGENTA color $RESET"
echo -e "$CYAN this is the CYAN color $RESET"
echo -e "$WHITE this is the WHITE color $RESET"